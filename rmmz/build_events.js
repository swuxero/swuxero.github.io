// 대사창 열기 (name=화자, bg: 0=윈도우 1=딤 2=투명)
const txtOpen = (name, bg=0, i=0) => c(101, i, ['', 0, bg, 2, name]);
const txtLine = (text, i=0)       => c(401, i, [text]);

function dlg(name, lines, i=0) {
  return [txtOpen(name, 0, i), ...lines.map(l => txtLine(l, i))];
}
function mono(lines, i=0) {
  return [txtOpen('', 0, i), ...lines.map(l => txtLine(l, i))];
}
function card(lines, i=0) {
  return [txtOpen('', 1, i), ...lines.map(l => txtLine(l, i))];
}

