function updateMap(mapId, events) {
  const file = path.join(DATA, `Map${String(mapId).padStart(3,'0')}.json`);
  const raw  = fs.readFileSync(file, 'utf8');
  const data = JSON.parse(raw);
  data.events = [null, ...events];
  fs.writeFileSync(file, JSON.stringify(data), 'utf8');
  console.log(`✔ Map${String(mapId).padStart(3,'0')}.json 업데이트 완료`);
}

function updateCommonEvent(ceId, list) {
  const file = path.join(DATA, 'CommonEvents.json');
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  data[ceId].list = list;
  fs.writeFileSync(file, JSON.stringify(data), 'utf8');
  console.log(`✔ CommonEvents[${ceId}] 업데이트 완료`);
}

