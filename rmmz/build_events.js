function makePage(list, trigger=3, selfCond=false, sw1Id=1, sw1Valid=false, img=null) {
  return {
    conditions: {
      actorId:1, actorValid:false,
      itemId:1,  itemValid:false,
      selfSwitchCh:'A', selfSwitchValid: selfCond,
      switch1Id: sw1Id, switch1Valid: sw1Valid,
      switch2Id:1, switch2Valid:false,
      variableId:1, variableValid:false, variableValue:0
    },
    directionFix: false,
    image: img || { characterIndex:0, characterName:'', direction:2, pattern:0, tileId:0 },
    list: [...list, end()],
    moveFrequency:3,
    moveRoute: { list:[{code:0,parameters:[]}], repeat:true, skippable:false, wait:false },
    moveSpeed:3, moveType:0, priorityType:1,
    stepAnime:false, through:false, trigger, walkAnime:true
  };
}

function npcImg(name, idx=0, dir=2) {
  return { characterIndex: idx, characterName: name, direction: dir, pattern:0, tileId:0 };
}

function makeEvent(id, name, x, y, pages) {
  return { id, name, note:'', pages, x, y };
}

const lockPage = makePage([], 0, true);

const makeNpcEv = (id, name, x, y, idx=1, dir=2) =>
  makeEvent(id, name, x, y, [
    makePage([], 0, false, 1, false, npcImg('Actor1', idx, dir)),
  ]);

